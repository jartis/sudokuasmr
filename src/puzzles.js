function getPuzzle() {
  const randomIndex = Math.floor(Math.random() * baseGrids.length);
  const baseGrid = baseGrids[randomIndex];
  const puz = [];
  for (let y = 0; y < 9; y += 1) {
    const row = [];
    for (let x = 0; x < 9; x += 1) {
      const index = (y * 9) + x;
      const value = parseInt(baseGrid[index], 10);
      row.push(value);
    }
    puz.push(row);
  }
  return puz;
};

